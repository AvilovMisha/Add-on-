  //get the category
  function get_category(id, host){
    for(var key in categories){
      var category = categories[key];
      for(var i=0; i<category.length; i++){
        if(category[i].host == host && ((Array.isArray(category[i].id) && category[i].id.indexOf(id) != -1) || (!Array.isArray(category[i].id) && category[i].id == id)) ) {
          return {name:key,  ids: category[i].id};
        }
      }
    }
    return false;
  }
  //Get Id
  function Getting_Id(sourceHost, targetId, targetHost){
        
        for(var key in categories){
          var category = categories[key];
          for(var i=0; i<category.length; i++){
            if(category[i].host == targetHost){
              if((Array.isArray(category[i].id) && category[i].id.indexOf(targetId) != -1) || category[i].id == targetId){
                for(var j=0; j<category.length; j++){
                  if(category[j].host == sourceHost){
                    return {name:key, ids: category[j].id};
                  }
                }
              }
            }
          }
        }
        return false;
        
  }   

